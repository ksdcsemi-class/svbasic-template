// Testbench
module tb;
   import mytypes::*;
   logic clk = 0;
   mode_t1 mode1;
   mode_t2 mode2;

   mone U1(.clk, .mode(mode1));
   mtwo U2(.clk, .mode(mode2));

   always #5 clk = ~clk;

   initial begin
      repeat (4) begin
         @(posedge clk);
         #1;
         $display("[Package] mode = %s (%0d)", mode1.name(), mode1);
         $display("[CUS]     mode = %s (%0d)", mode2.name(), mode2);
      end
      $finish;
   end
endmodule
